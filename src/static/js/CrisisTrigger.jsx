function norm(text) {
  return text
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "");
}

function stripVariants(word) {
  const v = [];
  for (let i = 0; i < word.length; i++) {
    v.push(word.slice(0, i) + word.slice(i + 1));
  }
  return v;
}

const BASE = [...new Set(CRISIS_KEYWORDS.map(norm))];
const FUZZY = [...new Set(BASE.filter((kw) => kw.length >= 6).flatMap(stripVariants))];

// Crisis detected = check all messages for N min
let crisisActiveUntil = 0;
const CRISIS_WINDOW_MS = 5 * 60 * 1000; // 5m

export function markCrisisDetected() {
  crisisActiveUntil = Date.now() + CRISIS_WINDOW_MS;
}

export function isCrisisWindowActive() {
  return Date.now() < crisisActiveUntil;
}

export function hasKeywordMatch(message) {
  const n = norm(message);
  if (BASE.some((kw) => n.includes(kw))) return true;
  return FUZZY.some((kw) => n.includes(kw));
}

export function shouldCheckCrisis(message) {
  if (isCrisisWindowActive()) return true;
  return hasKeywordMatch(message);
}
